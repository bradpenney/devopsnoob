                      <p>
                        Notice that these 5 processes are not represented in a linear fashion, but rather in a context-diagram fashion.  While the general goal is to start with initiating and move towards closing, its important to remember that project management is an iterative process - planning, executing, monitoring/controlling will all happen continuously, there is no specific order.  As the project progresses, planning will decrease and execution will increase.  However, its almost never too late to revist the planning stage, whether to clarify or to modify.  <strong>Project Managment is an iterative process!!</strong>
                      </p>
                      
