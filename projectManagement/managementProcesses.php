                        To clarify, this discussion is based on generic project managment processes - the methods of project management and the role of the project manager within these methods.  This discussion doesn't touch on the technical side of creating a product - clearly that sort of discussion is beyond the scope of any one website.  Please apply the methods and techniques being discussed here to the any business project, but realize anything related to a specific product will need its own set of requirements, processes and techniques.
                      </p>
                      <h3>5 Project Management Process Groups</h3>
                      <p>
                        Project management is sub-divided into 5 different processes.  Its really important to remember, however, that these processes are somewhat of an artificial distinction imposed "from above" by project managers (and project management theorists!).  For example, there is no separate "Monitoring and Controlling" stage - its built into the entire project.  Not one deliverable will be produced, not one business requirment will be agreed upon, without monitoring and controlling.  It is separate enough to be considered its own process, but its built into every aspect of the entire project.
                      </p>
                      <p>
                        Here are the five project managment process groups:
                      </p>
