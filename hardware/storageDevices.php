              <h3 id="hdd">Hard Disk Drive</h3>
              <figure>
                  <a title="By Evan-Amos (Own work) [CC BY-SA 3.0 (http://creativecommons.org/licenses/by-sa/3.0) or GFDL (http://www.gnu.org/copyleft/fdl.html)], via Wikimedia Commons" href="https://commons.wikimedia.org/wiki/File%3ALaptop-hard-drive-exposed.jpg"><img class="img-responsive centered" src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/f8/Laptop-hard-drive-exposed.jpg/512px-Laptop-hard-drive-exposed.jpg" alt="Laptop-hard-drive-exposed"/></a>
                  <figcaption class="text-center">2.5 Inch (Laptop) SATA Hard Drive (Case Open)</figcaption>
              </figure>
              <p>
                  Traditional hard drives have been storage devices for computers for decades.  The read-head travels across the metal disks (called platters).  The read-head finds the data it needs and reads it from the drive, or finds where it needs to write and writes to disk.  Depending on the hard disk, the platters spin at anywhere from 5,400rpm to 15,000rpm, with the faster speeds equating faster disk performance.  Disk performance is a major concern with traditional hard drives.  Traditional hard drives are usually internal to desktops and laptops, but can be attached externally via USB.
              </p>
              <h3 id="ssd">Solid State Drive (SSD)</h3>
              <figure>
                  <a href="https://commons.wikimedia.org/wiki/File%3AVertex_2_Solid_State_Drive_by_OCZ-top_oblique_PNr%C2%B00307.jpg"><img class="img-responsive centered" src="https://upload.wikimedia.org/wikipedia/commons/thumb/5/5e/Vertex_2_Solid_State_Drive_by_OCZ-top_oblique_PNr%C2%B00307.jpg/512px-Vertex_2_Solid_State_Drive_by_OCZ-top_oblique_PNr%C2%B00307.jpg" alt="Vertex 2 Solid State Drive by OCZ-top oblique PNr°0307"></a>
                  <figcaption class="text-center">2.5 Inch Solid State Drive (SSD) - Laptops and Desktops</figcaption>
              </figure>
              <p>
                  Solid-state drives (SSDs) are being integrated into most modern laptops and desktops. SSDs have no moving parts and work similar to <a href="https://aplusjourney.wordpress.com/2016/05/03/computer-memory-types-overview/">RAM</a>, except they are non-volatile.  They are being used as replacements or supplements to standard hard drives.  SSDs are more physically resilient and have faster access rates than traditional hard drives.  SSDs are significantly more expensive than traditional hard drives (for comparable storage space), but the price is continually dropping.
              </p>
              <h3 id="sshd">Solid-State Hybrid Drive (SSHDs)</h3>
              <p>
                  Solid-State Hybrid Drives (SSHDs) combine both a traditional hard drive and an SSD within the same device.  The SSD acts as a cache (buffer) for the spinning hard drive.  This combines the major advantage of SSD (faster access time) but retains the advantage of a traditional hard drive (cheap, large storage).  SSHDs look exactly the same as traditional hard drives, and will generally fit in the same bay inside the computer case (available in a 3.5-inch desktop format and a 2.5-inch laptop format).
              </p>
              <h3 id="opticalMedia">Optical Media</h3>
              <figure>
                  <a title="By Ubern00b (Own work) [GFDL (http://www.gnu.org/copyleft/fdl.html) or CC-BY-SA-3.0 (http://creativecommons.org/licenses/by-sa/3.0/)], via Wikimedia Commons" href="https://commons.wikimedia.org/wiki/File%3ACD_autolev_crop.jpg"><img class="img-responsive centered" src="https://upload.wikimedia.org/wikipedia/commons/thumb/d/d5/CD_autolev_crop.jpg/512px-CD_autolev_crop.jpg" alt="CD autolev crop"></a>
                  <figcaption class="text-center">Typical Compact Disc</figcaption>
              </figure>
              <p>
                  Common optical media formats include Compact Disc, Digital Video Disc, Blu-Ray Disc. Optical media store information in a series of bumps that can be read by a laser (see illustration below).
              </p>
              <figure>
                  <a href="https://commons.wikimedia.org/wiki/File%3ACompact_disk_data_layer_2d_3d.PNG"><img class="img-responsive centered" src="https://upload.wikimedia.org/wikipedia/commons/thumb/2/2c/Compact_disk_data_layer_2d_3d.PNG/256px-Compact_disk_data_layer_2d_3d.PNG" alt="Compact disk data layer 2d 3d"></a>
                  <figcaption class="text-center">Bumps on the Discs are Visible On a Micrometer Scale</figcaption>
              </figure>
              <p>
                  A Compact Disc can store about 700MB.  DVDs can store 4.7GBs, and 8.5GBs when they're dual layer.  Blu-Ray discs are the biggest optical drives, storing 25GB on a single layer, and 50GB on a dual-layer.
              </p>
              <p>
                  Optical media comes in read-only formats, but also writeable and re-writeable formats. Optical media writers don't create bumps like the optical media, rather they darken photosensitive dye that optical media drives read.
              </p>
              <h3 id="usb">USB Flash Drive</h3>
              <figure>
                  <a href="https://commons.wikimedia.org/wiki/File%3AUbuntu_USB_lanyard.jpg"><img class="img-responsive centered" src="https://upload.wikimedia.org/wikipedia/commons/2/2c/Ubuntu_USB_lanyard.jpg" alt="Ubuntu USB lanyard" /></a>
                  <figcaption class="text-center">USB Flash Drive</figcaption>
              </figure>
              <p>
                  USB flash drives (aka thumb drives) are an ultra-portable storage format. They are electrically erasable programmable memory (EEPROM).  Flash drives are non-volatile but aren't particularly robust and are prone to failure (and subsequent data loss).  For this reason, USB flash drives shouldn't be used to archive data, but are great for physically relocating data (to and from work or school for example).
              </p>
              <h3 id="tapeDrive">Tape Drive</h3>
              <figure>
                  <a href="https://commons.wikimedia.org/wiki/File%3ADds_tape_drive_01.jpg"><img class="img-responsive centered" src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/39/Dds_tape_drive_01.jpg/512px-Dds_tape_drive_01.jpg" alt="Dds tape drive 01"></a>
                  <figcaption class="text-center">Tape Drive</figcaption>
              </figure>
              <p>
                  A tape drive is an archival form of computer storage which, although very slow, is still in use today because tape drives offer huge amounts of storage for very low cost.  Tape drives are only used to archive data and are never used as a hard drive.
              </p>
              <p>
                  Tape drives are very robust and are very resilient in storage.  They are still actively used and are even under development, allowing massive storage for archival purposes.
              </p>
