              <h2 class="text-center">Storage Devices</h2>
              <p>
                  In computing, storage is the term for devices which continue to store data after the computer is powered off.  This means that storage devices include not only traditional hard drives but also SSDs, optical storage (CDs/DVDs/BluRay) and even tape drives. Storage is not the same as <a href="https://aplusjourney.wordpress.com/2016/05/03/computer-memory-types-overview/">memory (RAM)</a>.
              </p>
              <p>
                  This brief article will cover:
              </p>
